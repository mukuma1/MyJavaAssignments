
public class IfElseQ4 {

	public static void main(String[] args) {
		// Checking whether first# is a multiple of second
		
		int firstNumber = 105;
		int secondNumber = 5;
		if (firstNumber % secondNumber == 0) {
			System.out.println("The First Number " + firstNumber + " is a multiple of the Second Number " + secondNumber);
		} else
			System.out.println("The First Number " + firstNumber + " is NOT a multiple of the Second Number " + secondNumber);
	}

}
